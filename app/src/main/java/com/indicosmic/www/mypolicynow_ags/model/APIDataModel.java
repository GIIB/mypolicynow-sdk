package com.indicosmic.www.mypolicynow_ags.model;

public class APIDataModel {



    public String question_id;
    public String pos_id;
    public String proposal_list_id;
    public String ic_id;
    public String answer_id;
    public String image;
    public String break_in_case_id;


    public APIDataModel() {
    }

    public String getQuestion_id() {
        return question_id;
    }

    public void setQuestion_id(String question_id) {
        this.question_id = question_id;
    }

    public String getPos_id() {
        return pos_id;
    }

    public void setPos_id(String pos_id) {
        this.pos_id = pos_id;
    }

    public String getProposal_list_id() {
        return proposal_list_id;
    }

    public void setProposal_list_id(String proposal_list_id) {
        this.proposal_list_id = proposal_list_id;
    }

    public String getIc_id() {
        return ic_id;
    }

    public void setIc_id(String ic_id) {
        this.ic_id = ic_id;
    }

    public String getAnswer_id() {
        return answer_id;
    }

    public void setAnswer_id(String answer_id) {
        this.answer_id = answer_id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getBreak_in_case_id() {
        return break_in_case_id;
    }

    public void setBreak_in_case_id(String break_in_case_id) {
        this.break_in_case_id = break_in_case_id;
    }
}
